package lec03_swapvariables;

public class Main {

	public static void main(String[] args) {
		
		int x=23;
		int y=46;
		
		System.out.println("x: "+x);
		System.out.println("y: "+y);
		System.out.println();
		
		int temp;
		
		temp=x;
		x=y;
		y=temp;
		
		System.out.println("x: "+x);
		System.out.println("y: "+y);
		
		
				
		
		
		
		
	}

}
