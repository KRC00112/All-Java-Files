public class number7 {

//    7. Write a program to print all the even numbers between two
//    integer variables x and y.
    public number7(){

        int x=2;
        int y=15;


        System.out.println("All the numbers between "+x+" and "+y+" are: ");


        for(int i=x;i<=y;i++){

            if(i%2==0){

                System.out.print(i+" ");


            }


        }


    }

}
