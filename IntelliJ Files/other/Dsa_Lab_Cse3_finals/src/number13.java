public class number13 {

//    13. Write a program to print all the odd numbers between two
//    integer variables x and y.

    public number13(){

        int x=5;
        int y=20;


        for(int i=x;i<=y;i++){

            if(i%2!=0){
                System.out.println(i+" ");
            }


        }




    }

}
